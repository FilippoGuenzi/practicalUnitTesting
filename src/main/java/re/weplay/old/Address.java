package re.weplay.old;

public class Address
{
    private String detail;

    public Address(String pDetail)
    {
        super();
        detail = pDetail;
    }

    public Address()
    {
        super();
    }

    public String getDetail()
    {
        return detail;
    }

    public void setDetail(String pDetail)
    {
        detail = pDetail;
    }

}
